namespace ConsoleInput
{
    /// <summary>
    /// Enumeration of buttons on a mouse.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>
        /// The leftmost mouse button.
        /// </summary>
        Left = 0,

        /// <summary>
        /// The second button fom the left.
        /// </summary>
        Right = 1,

        /// <summary>
        /// The third button from the left. Commonly, the mouse wheel button.
        /// </summary>
        MouseWheel = 2,

        /// <summary>
        /// The fourth button from the left.
        /// </summary>
        FourthButton = 3,

        /// <summary>
        /// The rightmost mouse button.
        /// </summary>
        FifthButton = 4,
    }
}
